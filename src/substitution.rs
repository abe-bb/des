use bitvec::vec::BitVec;

const SBOX_INPUT_OFFSET: usize = 6;
const SBOX_ROW_LEN: usize = 16;

pub fn s_boxes(input: BitVec) -> BitVec {
    let mut output: BitVec = BitVec::with_capacity(32);
    assert_eq!(48, input.len());
    for i in 0..8 {
        // determine row
        let mut row = input[i * SBOX_INPUT_OFFSET] as usize | 0;
        row <<= 1;
        row |= input[i * SBOX_INPUT_OFFSET + 5] as usize;
        // determine column
        let mut column = input[i * SBOX_INPUT_OFFSET + 1] as usize;
        for j in 2..5 {
            column <<= 1;
            column |= input[i * SBOX_INPUT_OFFSET + j] as usize;
        }
        // determine s_box array index
        let s_box_index = row * SBOX_ROW_LEN + column;
        let substitute = TABLES[i][s_box_index];

        // push substitute bits onto the output
        for j in (0..4).rev() {
            output.push((substitute >> j & 1) == 1);
        }
    }

    output
}

